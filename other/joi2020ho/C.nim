when isMainModule:
  var N, L: int
  (N, L) = inputInts()
  var X, T: array[210, int]
  for i, xi in inputInts():
    X[i + 1] = xi
  for i, ti in inputInts():
    T[i + 1] = ti
  X[N + 1] = L
  
  const INF = 10^18
  var dp: array[210, array[210, array[2, array[201, int]]]]
  for i in 0..N + 1:
    for j in 0..N + 1:
      for k in 0..1:
        for m in 0..N:
          dp[i][j][k][m] = INF
  
  var ans = 0
  dp[0][N + 1][0][0] = 0
  dp[0][N + 1][1][0] = 0
  for left in 0..<N:
    for right in countdown(N + 1, left + 2):
      for cnt in 0..<N:
        var t = min(
          dp[left][right][0][cnt] + X[left + 1] - X[left],
          dp[left][right][1][cnt] + L - X[right] + X[left + 1]
        )
        var ncnt = cnt
        if t <= T[left + 1]:
          ncnt.inc
          ans.chmax(ncnt)
        dp[left + 1][right][0][ncnt].chmin(t)
        
        t = min(
          dp[left][right][0][cnt] + L - X[right - 1] + X[left],
          dp[left][right][1][cnt] + X[right] - X[right - 1]
        )
        ncnt = cnt
        if t <= T[right - 1]:
          ncnt.inc
          ans.chmax(ncnt)
        dp[left][right - 1][1][ncnt].chmin(t)
  echo ans
