proc matMul*(A, B: seq[seq[int]], M=10^9 + 7): seq[seq[int]] =
  let
    N0 = A.len
    N1 = B[0].len
    N2 = A[0].len
  result = newSeqWith(N0, newSeq[int](N1))
  for i in 0..<N0:
    for j in 0..<N1:
      for k in 0..<N2:
        result[i][j] = floorMod(result[i][j] + A[i][k]*B[k][j], M)

proc matPow*(A: seq[seq[int]], K: int, M=10^9 + 7): seq[seq[int]] =
  let N = len(A)
  var
    A = A
    K = K
  result = newSeqWith(N, newSeq[int](N))
  for i in 0..<N:
    result[i][i] = 1
  while K > 0:
    if bitand(K, 1) == 1:
      result = matMul(result, A, M)
    A = matMul(A, A, M)
    K = K shr 1

when isMainModule:
  const MOD = 998244353
  var H, W: int
  (H, W) = inputInts()

  var mat = newSeqWith(2^H, newSeq[int](2^H))

  proc dfs(cur, fr, to: int) =
    if cur == H:
      mat[fr][to].inc
      return

    dfs(cur + 1, fr, to)
    if bitand(fr shr cur, 1) == 0:
      dfs(cur + 1, fr, bitor(to, 1 shl cur))
      if cur < H - 1 and bitand(fr shr (cur + 1), 1) == 0:
        dfs(cur + 2, fr, to)
  
  for s in 0..<2^H:
    dfs(0, s, 0)
  
  let ans = matPow(mat, W, MOD)[0][0]
  echo ans
